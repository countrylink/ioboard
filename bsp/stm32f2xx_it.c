void TIM2_IRQHandler(void)
{
    TIM_ClearITPendingBit(TIM2, TIM_IT_Update);
    ADC_SPI_handler ();
    return;
}

void SPI2_IRQHandler (void)
{
    if ((SPI2->SR & SPI_I2S_FLAG_RXNE) == (uint16_t)SET) {
         RxBuf[RxWr++] = SPI2->DR;
         if (RxWr >= RX_SIZE) RxWr = 0;
         RxFullness++;
    }

}

